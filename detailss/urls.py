from django.contrib import admin
from django.urls import path
from detailss import views
from rest_framework.urlpatterns import format_suffix_patterns
from rest_framework_simplejwt.views import (
    TokenObtainPairView,
    TokenRefreshView,
)
from detailss.views import RegisterAPIView

urlpatterns = [
    path('admin/', admin.site.urls),
    path('drinks/',views.drinklist),
    path('drinks/<int:id>',views.details),
    path('api/login/', TokenObtainPairView.as_view(), name='login_view'),
    path('api/token/refresh/', TokenRefreshView.as_view(), name='token_refresh_view'),
    path('api/register/', RegisterAPIView.as_view(), name='register_view'),
]

urlpatterns = format_suffix_patterns(urlpatterns)